            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    The irony that India capacitates the existence of Jio, the brainchild of Mukesh Ambani, one of the five richest men in the world, is lost on a carpenter who recently purchased a network subscription. With the advent of Digital India, today the carpenter might be thrilled with his new subscription, but tomorrow he may realize his plight as an “unskilled” labourer, unfit for a job in the same company that is his network provider.  Such is the paradox instituted by inequality in India, where progress is counterintuitive to the very cause that it is “progressing” towards. Unemployment in India, too, is a part of the deep-rooted cyclical trap that encompasses the whole country’s step forward in reaching a resolution.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Evidenced by the fact that approximately 86% of the total employment in the country is unorganized even today, Digital India has a long way to go. About 97% of the economic activity in agriculture, 74% in construction, and 86% in trade, repair, and food services, stands unorganized.<sup>1</sup> In addition to the large-scale employment supported by the unorganised sector, the nature of associated jobs also makes workers vulnerable to condescension to the extent that it quells all of their demands. Labour laws, here, have a very important role to play. However, much has been said about the restrictive nature of these laws. With around 40 laws of the central government and 100 of them from the state government, it has become increasingly difficult to juggle between complying with both the center and the state. For example, the “hire and fire” law that was criticized by unions to give much more freedom to employers, also demanded that the center intervene in its implementation process. This was also in light of the fact that due to mechanisation fewer jobs would be made available to workers if further liberalisation of this policy continued.<sup>2</sup>
                </p>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    Speaking of mechanisation, just when agriculture became an occupational activity too obsolete to those who migrated to the manufacturing sector, automation and other advancements in the IT industry rendered the newly sought-after-occupation as obsolete too. While the IT industry has created jobs, nonetheless, these have not compensated for the jobs that were laid off. Since most of these jobs highly demand fluency in coding languages such as Python, it becomes a problem for those who cannot even afford a smartphone. The generation of jobs might thus be heading where there is no workforce available at all. Paradoxically, any policy advancement in India (in this case Digital India) almost always faces an equal and opposite reaction - the reformed manufacturing sector with lesser jobs on offer. What is worse is that this does not only make a population unemployed but also casts a deeper meaning behind its inability – that of unsuitability.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    To begin the talk around unsuitability, India is currently undergoing a demographic transition in its workforce. The demographic dividend defined by the UN as “the economic growth potential that can result from shifts in a population’s age structure”, seemed at first advantageous to the country in garnering potential employment, given India’s massive youth population. However, besides the bleak picture painted by the unemployment rate and the labour force participation rate, the youth unemployment rate has not shown a promising figure either. This is surprising, especially because it also means that the efforts of government initiatives like Skill India and Make in India are simply not enough. These initiatives have clearly come about after the realization of the fact the Indian populace is not skilled enough to make use of even remarkable policy initiatives like MGNREGA.  Barring the unprecedented effects of the pandemic this year, in 2019, the youth unemployment rate stood at 23% which was an increase of approximately 1 % from the previous year.
                </p>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    After having realized the limitations of MGNREGA, the government launched the Make in India program in 2014 to promote self-reliance. While this policy is certainly laudable for its admirable goals, it is also important to evaluate the suitability of its drivers in reaching its targets. Raghuram Rajan, former Governor of RBI, highlighted several other precedents that are required before actually making advances in this direction. These included proper infrastructural growth (roads, railways, airports, ports), better electronic connectivity, financial stability, and healthcare facilities.<sup>3</sup> An important question arises when we look at the rural-urban divide representing irreconcilable backgrounds of its diverse people, the credit crisis of banks, and the deplorable living conditions of its malnourished youth - can we really say that people are fit enough to avail the benefits of a policy reform that asks more from those who have very little to give? To say that Make in India or any other skill development program isn’t going to amount to anything is also a very fallacious statement. However, under the National Skill Development Corporation in 2016, only about 12% of the 557,000 that became “skilled” were able to find jobs.<sup>4</sup>
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    To trace the unsuitability of our populace, one would have to search the deeper ideological pattern observed within its society through the remnants of the past. Capitalistic societies have developed across all parts of the world, but what makes the one in India different from that in the richest of countries? To question whether or not our people are too slow to cope with the fast-paced world would come only second to questioning the target site of public policy reforms that institutionalize public welfare. While policy reforms like Make in India and Digital India are extremely valuable to the state of the economy at present, they are being rendered only partially successful. We still need more job creation, given that each year approximately 12 million Indians enter the workforce. We still need to provide soft-skills training to cater to the demands of the growing technological world. And we still need to derive more from the potentiality of our currently underskilled population. Following up with the discussion that surrounds any policy action, the issue of ‘implementation’ has gathered much skepticism.
                </p>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    Likewise, if the unemployment problem is one that has become difficult to tackle, it is also a reflection of past policies that have rendered its population unable to grasp hold of opportunities like Skill India and Make in India. When the Demonetization of the Indian currency took place, it exacerbated the unemployment levels even further. This was bound to happen, considering the huge role of the unorganised sector in our economy that largely deals in cash. Hence, given the targets that it had outlined upon commencement, demonetization, a step that the RBI was itself at odds with, failed to achieve them. Sure, it led to a faster digitalization of the country, but only after its people had succumbed to joblessness. The demonetized money only accounted for 20% of the black money, which is very little compared to the disruption it had caused in the economy.<sup>5</sup> With as diverse a population as the one that split into Telangana and Andhra Pradesh only recently in 2014, it is understandably very difficult to come up with a blanket policy. But even if one were to adopt a blanket policy in this country, it certainly shouldn’t have been one that does very little benefit in the case of the minority while causing huge damage to the majority. With over 75% of the working-age population engaged in the informal economy, policies like demonetization coupled with GST were bound to hit hard on the Indian economy.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    What is also problematic in our discussions of the composition of the workforce in our country is the lack of focus on the role of female employment in aiding our employment scenarios. Astonishing yet predictably, India’s potential female population could amount to the country becoming 27% richer.<sup>6</sup> Yet, the realization of this fact has been submerged by the conservative mindset of the Indian society in preferring their women at home. While women are willing to work,  our society isn’t conducive enough to welcome them into workspaces, in addition to the growth in fertility and illiteracy rates that bind them at home. Besides, the fact that women-safe workspaces are also very few in numbers serves as a proof that the lack of hostility against rape-culture is the overall stereotypical mindset of our society.
                </p>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    To enter the blame-game of who really is responsible for the unemployment crisis in India, is a lengthy conversation. The true understanding of why our policies fall short in meeting their goals is an indication of how unreconciled the goals of the government and the general public have become. This makes the role of education very important in trying to reunite the government and the public in the realisation of a shared goal – of full utilization of the potentiality of our populace. Only recently has the government introduced the NEP, a liberalized approach to learning, advocating critical thinking, and promoting students to widen their learning capacities. To grow as a people is, indeed, imperative to growing as an economy. Increasing aggregate demand to curb involuntary unemployment, training programs offered as a solution to tackle frictional unemployment and inflationary policies to avoid cyclical unemployment<sup>7</sup> are just short-term solutions to a problem that is in nature of its affliction, evergreen, if not realized. The question, therefore, remains - Will the said carpenter ever realize his capability in working as a software developer for Jio or accept his destiny as a mere security guard instead?
                </p>
