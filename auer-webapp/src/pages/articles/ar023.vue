                <div class='title-url' style="font-size:35px; line-height:60px; font-family: 'Rufina', serif; text-align:left">
                  Nobel Prize 2020
                </div>
                <div class='title-url' style="font-size:1.5rem; line-height:1.4; font-weight:700; font-family: 'Rufina', serif; text-size-adjust:100%; text-align:left ">
                  Going, Going and Gone to Stanford’s Paul Milgrom and Robert Wilson
                </div>
                <br />
                <div class='row'>
                    <div>
                        <div class="text-subtitle2" style="font-family:'Rufina', serif">by Anirudh Pisharam and Paroma Roy Chowdhury | October 16th, 2020</div>
                    </div>
                </div>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <i style="font-size: 14px;font-family: 'Rufina', serif">Paul Milgrom, favoured by many to win the 2020 Sveriges Riksbank Prize in economics, found out the news of his win in a rather unusual manner—through a 2.15 AM ring to his doorbell by his PhD advisor and co-winner Robert Wilson! In this article the authors examine some of Milgrom and Wilson’s pioneering contributions to the area of auction theory which won them the prestigious 2020 Nobel Prize.
                </i>
            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    The biggest annual announcement in Economics brought wider acknowledgement to Stanford economists Robert Wilson and his doctoral student Paul Milgrom. Both are veteran game theorists who have made several crucial contributions towards the field of microeconomic theory as well as applied microeconomics. Nobel laureate Al Roth lauded them as “some of the greatest theorists living in economics today” (Smialek, 2020). However, it was their seminal work for “improvements to auction theory and inventions of new auction format” that the Royal Swedish Academy for Science chose to recognize. Both Wilson and Milgrom, (along with the broader Stanford school economists like Roth) helped bridge theory and practice through the concept of market design. This involves designing the rules of the game such that participants involved in the game engage in optimal actions that help the designer achieve their objects. Tweaking auction formats to meet objectives such as revenue maximization is an application of market design. In order to better understand the type of auctions Milgrom and Wilson helped design, it is useful to look at different kinds of auctions.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Standard auctions usually are private value auctions. In this setting, individuals have their own subjective valuation of the goods being auctioned, which can differ across the bidders. Under certain conditions, an important result known as the revenue equivalence theorem shows that the seller is expected to earn the same revenue irrespective of the auction format that is chosen. This is an important result since it obviates the need for any particular format, since they all give the seller identical expected revenue from sale.
                </p>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    Another important class of allocation decisions involves settings where valuations are not private. A classic example is an auction for a jar containing an unknown number of coins. In such auctions, all bidders have an identical valuation of the good being auctioned, even as this value is not public knowledge. In these common value auctions, petroleum engineers, noticed that the rates of return on oil fields were extremely low, suggesting that the “successful bidders might not have been so successful after all” (Capen et al., 1971). They reasoned since the auction is won by the highest bidder – who usually also happens to have the most optimistic valuation – they tend to overpay. This phenomenon came to be known as the “winners’ curse” and is a central feature of common value auctions that leads to bidders strategizing by discounting their bids in order to avoid losses. However, this discounting also means that the sellers of the object end up getting lower revenue. In such a scenario, the chosen auction format could impact the revenue earned by the seller.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Further, auctions are often conducted with different incentives for the auctioneer. For instance while the goal in an art auction might be to maximise profits, a government selling construction rights to private entities might want to minimize costs and ensure high quality construction. Auctions for medical equipment might have an entirely different objective of maximising social welfare. In these varied settings, therefore how auctions are designed can have important implications on outcomes. Here, Wilson and Milgrom made several important theoretical contributions to the field, starting with Wilson in the late 1960s and 1970s. He wrote multiple papers harnessing the new found game-theory tools that helped economists’ model strategic behaviour. This culminated in Wilson (1977) which formulated a Nash Equilibrium solution for first-price common value auctions, which was then generalized by Milgrom and Weber (1982) (Kagel and Levin, 1986). The latter paper made an important insight – English auctions outperform sealed bid second-price auctions. Additionally, they found that “adopting a policy of expert appraisal of the quality of the objects” being sold would improve revenues for the seller.
                </p>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
                    Until the late twentieth century important government resources like spectrum were allocated
to firms either through lotteries or “beauty contests”, which involved interested parties
pleading their case for use of bandwidth. These mechanisms did not generate any significant
revenues for the government. In the early 1990s, the US Federal Communication Commission
(FCC) decided to change their approach, deciding to consult game theorists to design a better
mechanism of spectrum allocation. Together with economist Preston McAfee, Milgrom and
Wilson used their work to help design a brand-new auction technique called Simultaneous
Multiple Round auctions (SMRA). This format allowed interested parties to simultaneously
bid on multiple bundles of region-spectrum that are being auctioned in every round. At
the end of each round, the highest bid is revealed to the parties who can then update their
beliefs of the common-value of different bundles and change their strategies accordingly. The
auction then ends when no party has increased their bids on any of the bundles, post which
the highest bidder is allocated their bundle.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    First used in 1994, the results astounded everyone as the FCC raised over $600 Million from
sales to different users. Unsurprisingly, these auction formats have grown in popularity and
have been adapted to a wide range of common-value auctions across the globe, including
electricity production and oil leases among others. Milgrom has continued to contribute
to auction design with newer formats including clock-proxy auction (Ausubel et al., 2006).
He also spearheaded the 2012 FCC incentive auctions, to fast-track the 5G spectrum in a
faster timeframe than a political process and transfer existing spectrum rights from lower
performing TV broadcasters to more efficient mobile broadband, generating a system of compensation that losing parties were willing to accept. Overall, their work has also helped
further the idea of the “Economist as an Engineer” (Roth, 2002), who can design rules and
shape institutions to achieve certain socially desirable objectives.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    With the internet, auctions broadly have permeated into many spheres of our lives, even as
formats like the SMRA may seem eclectic and different to our notions of an auctioneer selling
a single item to the highest bidder. Online auctions in eBay are perhaps more commonly
known, as they embody the typical auction format that awards an item to the highest
bidder. Auctions in electricity generation call for their own set of unique considerations
taking into account peak hour demand, transmission etc, and are part of a larger conversation
to restructure electricity markets more efficiently. Yet another example has risen in the wake
of the looming climate crisis where many countries have chosen to establish markets for
emission rights which are conducted via auctions as opposed to direct taxation.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Both auctions themselves and the nature of the goods sold have fast evolved to attain the high
levels of complexity evident in Milgrom and Wilson’s work but what truly sets apart their
approach to auction and market design is their recognition of inherent complementarities in
the goods sold and the interaction of many different factors within the system of participants.
This necessitates market formats that need to account for assembly of efficient bundles of
goods by participants, allow competition among bidders to keep prices reasonable and also
bestow sufficient control on the auctioning process to facilitate equitable allocations and
prevent possible ethical nightmares such as black markets for organs (an area Roth has
contributed extensively to). Even in the current pandemic, Milgrom and Wilson’s work
promises to contribute to superior allocative processes that could once only be theorized
about on paper. For instance, in their Nobel press conference, Wilson referred to his work
with Peter Crampton and Al Roth to suggest more efficient allocation of PPE, to improve the
utilisation of existing and acquisition of new resources in future moments of scarcity Stanford
(12). One possible manner of allocation discussed was based on projected number of patients
in a geographical vicinity, and allowing a market where hospitals could dynamically exchange
PPE based on present needs using an artificial currency to achieve efficiency. Milgrom
also highlighted the need for better market design and well thought out systems that can
withstand the stiff demands of allocation in a crisis and could prevent unfortunate situations
such as US states competing against each other and bidding up ventilator prices in the early
days of the Covid-19 crisis.
                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
                    Therefore, as we live through times where the momentousness of Milgrom and Wilson’s research seems to only be growing, the prize perhaps highlights not only their stellar past
contributions but the need for more ‘economist-engineers’ who can gauge the growing functional demands of modern markets. Moreover, with Milgrom being the third of Wilson’s
doctoral students to win a Nobel prize following Roth and Holmstr¨om, one might very well
imagine that some of these innovations could emerge from the graduate students these great
minds have nurtured.
                </p>
