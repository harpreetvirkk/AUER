           <p style="font-size: 18px;font-family: 'Rufina', serif">
On analysing both trailing as well as forward P/E, we see that the current Nifty values are definitely in the overvalued region. Even though the future EPS growth prospects seem optimistic, a correction or a sideways trend to bring Nifty P/E back to its long term average can be expected.
           </p>
           <b style="font-size: 18px;font-family: 'Rufina', serif">
2. Shiller P/E Ratio (CAPE)
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
The Shiller Ratio or the Cyclically Adjusted Price Earning (CAPE) Ratio is a more robust analysis than simple P/E calculations. This smoothens the P/E curve by adjusting for inflation and business cycles. The long run 20-year Shiller P/E average for Nifty is 22.7. The latest data on CAPE ratio is from December 31, 2020 where the Shiller P/E stood at 29.91. As the markets have gone up by 12.2% since then, simple extrapolation suggests that current CAPE ratio stands at 33.56. This is higher than the long term average indicating that Nifty might be currently overvalued.           </p>
            <div class="col-md-8" style="padding: 10px 40px 10px 40px">
