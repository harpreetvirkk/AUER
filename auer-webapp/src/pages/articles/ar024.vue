3. Dividend Yield
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
In general, when stocks are cheap dividend yields are high, and when stocks are overpriced, dividend yields are low. The current Nifty 50 dividend yield is at 1.09 which is lower than the long term average of 1.422 indicating that Nifty is currently overvalued. The following chart shows the Nifty 50 dividend yield for the last 15 years along with the long term average.
