The following piece aims to analyse current Nifty levels through economic fundamentals. The article uses 5 metrics namely P/E ratio, CAPE ratio, Dividend Yields, Interest Rates and Buffett Indicator to assess whether the recent market exuberance is in line with the economic recovery.</i>            </div>
            <div style="padding: 10px 0px 10px 0px">
                <p style="font-size: 18px; font-family: 'Rufina', serif">
Hovering around ₹15,900 levels, Nifty 50 is at its all time high with recovering more than 100% since its lows from March 23, 2020 after a nationwide lockdown was announced. Even though we’re in the midst of a second Covid wave and the onset of a possible third, markets continue to soar and are much more resilient to the pandemic now than they were during the first wave. This market exuberance and optimism raises natural questions for every investor – why are markets rising the way they are? Is a significant correction on the cards for near future? And what should retail investors really do in a market that is piquing record highs?                </p>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
Before attempting to answer these questions, it is imperative to analyse this market exuberance in terms of valuations. Many analysts and fund managers have contrasting views on this matter – some suggesting that markets have risen way above their fundamentals, ignoring macro signals from a plunging economy whereas others still believing that this is just the beginning of a major bull run. The RBI in its FY21 report also warned investors of a possible stock market bubble, suggesting that the price boom seen in some stocks exceeded their intrinsic value. In order to gauge whether these markets are fairly valued, 5 market indicators are extensively used by analysts all across the world. These indicators are P/E Ratio, Shiller P/E Ratio, Dividend Yield, BEER Ratio and finally Market Cap to GDP Ratio.
                </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
1. Price to Earnings Ratio
                </b>
                <p style="font-size: 18px; font-family: 'Rufina', serif">
The first and the most basic metric that is used to assess the valuation of the overall market is the P/E ratio. This ratio is popularly used to determine whether the price you pay or receive while transacting in the stock market is too high or too low. In order to comprehensively analyse Nifty P/E, it is important to look at both Trailing and Forward P/E ratios. Trailing P/E is calculated by dividing the Nifty price with the earnings per share (EPS) over the last 12 months. Similarly, Forward P/E is the current Nifty Price divided by the expected earnings over the next 12 months.                </p>
                <!-- </p> -->
                <b style="font-size: 18px;font-family: 'Rufina', serif">
<i>Trailing P/E</i>
                </b>
