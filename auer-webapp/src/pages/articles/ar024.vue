            <b style="font-size: 18px;font-family: 'Rufina', serif">
4. Interest Rates and BEER
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
Another method to analyse market levels is by looking at the 10 year government bond yields. These bonds are considered to represent the risk free rate of return. Hence, if the G-Bond rates are higher, people would invest more money in government securities and the demand for stocks will reduce, driving their prices low. Contrastingly, if the government bond yields are low, this would induce a higher demand for stocks, thus pushing stock prices higher. The following chart shows the 10 year G-Bond yield rates over the last 15 years. The current yield rate is at 6.113, which is among the lowest since the last 15 years barring the 2007-08 financial crisis. These low interest rates have inflated stock prices as well. Even though the price increase can be justified by higher earnings, these stocks do seem expensive from a valuation point of view. A lower interest rate on government bond thus increases the P/E ratio as well.
           </p>
           <div class="col-md-8" style="padding: 10px 40px 10px 40px">
                <q-img
                src="../../assets/ar024/ar024_7.png"
                style="width: 100%, height: auto"
