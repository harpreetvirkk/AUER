                <p style="font-size: 18px;font-family: 'Rufina', serif">
Currently the Nifty trailing P/E recorded at the <b>end of June, 2021</b> is <b>29.13</b> based on data from NSE. As can be seen from the chart above, the <b>10-year long term average</b> of Nifty P/E stands at a mere <b>23.27</b>. Even though Nifty P/E has fallen significantly over the past few months, where it touched 40 in the months of January and February, the current value is still above the long term average indicating that it still might be in an overvalued zone.
            </p>
                <b style="font-size: 18px;font-family: 'Rufina', serif">
<i>Forward P/E</i>
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
The idea behind this ratio is to understand what is the required growth in EPS to get a Forward P/E ratio equal to the long term average of 23.27 and whether this growth rate is reasonably achievable in the next 12 months. As of June 30, 2021, the Nifty50 EPS stands at 554.9. The following table calculates the required EPS growth such that the Forward P/E is equal to the long term average.              </p>
