This means that in order for P/E to return to its long term average in the next 12 months at the current Nifty price the require EPS growth rate is 21.58%. However this seems farfetched considering we are still no where close to complete vaccination and the onset of a third wave might induce a new set of lockdowns across the country. Further, the recent RBI forecast expects a GDP growth of 9.5% in the current fiscal year, which can be taken as a proxy for EPS growth rate.
           </p>
