           <p style="font-size: 18px;font-family: 'Rufina', serif">
Theoretically, a BEER ratio above 1 indicates that the stock market is overvalued. However, the long term BEER for Nifty has been around 1.5%. Thus, on comparing the current BEER with its long term average we see that Nifty, even though slightly, is still overvalued.
           </p>
           <b style="font-size: 18px;font-family: 'Rufina', serif">
           5. Market Cap to GDP Ratio
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
Also known as the Buffett indicator, named after the famous investor Warren Buffett, Market Cap to GDP ratio is perhaps the best single measure to analyse where valuations stand at any given moment. It is calculated by dividing the current Market cap of all publicly traded stocks and divide that by the nominal GDP level of the country. The following table shows the calculation for the Buffet indicator for India as well as some of the other major economies and stock markets.
           </p>
