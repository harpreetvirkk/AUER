                </q-img>
            </div>
           <p style="font-size: 18px;font-family: 'Rufina', serif">
In order to comprehensively analyse interest rates, we shall use another metric known as Bond Equity Earnings Yield Ratio (BEER). BEER is a metric that is calculated by dividing the long term government bond yield rates by the equity earnings yield of the stock market. Equity earnings yield is calculated by dividing earnings per share by the price per share (reciprocal of the P/E ratio). The current BEER is calculated in the table below.
           </p>
