<p style="font-size: 18px;font-family: 'Rufina', serif">
As we can see, based on the Buffett indicator, the Indian stock markets are significantly overvalued and so are some of the other major stock markets of the world except the Shanghai Stock Exchange, which is well within the fairly valued zone.
           </p>
<p style="font-size: 18px;font-family: 'Rufina', serif">
However, it is also important to note that most of the Indian economy still falls under the informal sector and publicly listed companies do not represent that part of the economy. Thus, the buffet indicator might not be the most ideal metric to judge the Indian stock market, however it still gives us some important comparison statistics to understand market valuations.           </p>
           <b style="font-size: 18px;font-family: 'Rufina', serif">
Conclusion
                </b>
                <p style="font-size: 18px;font-family: 'Rufina', serif">
On analysing the economic fundamentals for the Indian stock market we see that all indicators suggest that the current levels of Nifty are not sustainable. With the future of the pandemic still uncertain and only 5% of the population being vaccinated, corporate earnings could still take a toll in the near future. Markets have been showing a sideways trend since the end of June and any entry now would be a speculative move.  In the past few months, record highs have been driven by improved potential corporate earnings and profitability as many companies have responded to the pandemic by cost cutting measures and digitalisation. However, there are significant downside risks like rising inflation and increasing commodity prices like that of crude. Thus, investors should be careful in the equity markets right now. Investors should make sure that any stock entry should be carefully calculated and be based on strong fundamental analysis and with future prospects of the company, industry and the economy in mind. A significant correction might be on the cards and a favourable entry point might open in future.
